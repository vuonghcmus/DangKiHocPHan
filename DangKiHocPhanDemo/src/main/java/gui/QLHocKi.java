    private boolean checkNgayLonHon(String[] s1, String[] s2) {
        for (int i = 2; i >= 0; i--) {
            if ((s1[i]).compareTo(s2[i]) < 0) {
                return false;
            } else if ((s1[i]).compareTo(s2[i]) > 0) {
                return true;
            }
        }
        return true;
    }

    private void btnThemTab2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemTab2ActionPerformed
        String name = cbHocKiTab2.getSelectedItem().toString();
        String year = cbYearTab2.getSelectedItem().toString();

        if (cb3Tab2.getSelectedItem() == null || cb6Tab2.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(rootPane, "Ngày không được rỗng!");
            return;
        }

        List<semester> listSemester = semesterDAO.layDanhSachSemester();
        for (semester s : listSemester) {
            if (name.equals(s.getName()) && year.equals(s.getYear())) {
                JOptionPane.showMessageDialog(rootPane, "Học kỳ đã tồn tại!");
                return;
            }
        }

        String[] NgayBatDau = {cb3Tab2.getSelectedItem().toString(), cb2Tab2.getSelectedItem().toString(),
            cb1Tab2.getSelectedItem().toString()};
        String[] NgayKetThuc = {cb6Tab2.getSelectedItem().toString(), cb5Tab2.getSelectedItem().toString(),
            cb4Tab2.getSelectedItem().toString()};

        if (checkNgayLonHon(NgayBatDau, NgayKetThuc) == true) {
            JOptionPane.showMessageDialog(rootPane, "Ngày kết thúc học kì phải sau ngày bắt đầu học kì!");
            return;
        }

        String ngayBatDau = cb3Tab2.getSelectedItem().toString() + "-" + cb2Tab2.getSelectedItem().toString()
                + "-" + cb1Tab2.getSelectedItem().toString();
        String ngayKetThuc = cb6Tab2.getSelectedItem().toString() + "-" + cb5Tab2.getSelectedItem().toString()
                + "-" + cb4Tab2.getSelectedItem().toString();
        semester u = new semester(ngayBatDau, year, name, ngayKetThuc, 0);
        if (semesterDAO.addSemester(u) == true) {
            JOptionPane.showMessageDialog(rootPane, "Thêm học kỳ thành công!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Thời gian bắt đầu học kỳ đã tồn tại!");
        }
    }//GEN-LAST:event_btnThemTab2ActionPerformed

