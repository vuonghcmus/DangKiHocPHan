    private void btnTab4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTab4ActionPerformed
        int count = 0;

        for (int i = 0; i < modelTab4.getRowCount(); i++) {
            if ((boolean) modelTab4.getValueAt(i, 0) == true) {
                count++;
            }
        }

        if (count == 0) {
            JOptionPane.showMessageDialog(rootPane, "Chưa set học kỳ nào làm học kỳ hiện tại!");
            return;
        } else if (count > 1) {
            JOptionPane.showMessageDialog(rootPane, "Chỉ được set 1 học kỳ làm học kỳ hiện tại!");
            return;
        }

        for (int i = 0; i < modelTab4.getRowCount(); i++) {
            if ((boolean) modelTab4.getValueAt(i, 0) == true) {
                String name = (String) modelTab4.getValueAt(i, 1);
                String year = (String) modelTab4.getValueAt(i, 2);
                semester s = semesterDAO.layHocKyVaNamCuaSemester(name, year);
                s.setHocKyHienTai(1);
                if (semesterDAO.capNhatThongTinSemester(s) == true) {
                    JOptionPane.showMessageDialog(rootPane, "Set học kỳ thành công!");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Set học kỳ thất bại!");
                }
            } else {
                String name = (String) modelTab4.getValueAt(i, 1);
                String year = (String) modelTab4.getValueAt(i, 2);
                semester s = semesterDAO.layHocKyVaNamCuaSemester(name, year);
                s.setHocKyHienTai(0);
                if (semesterDAO.capNhatThongTinSemester(s) == true) {
                    System.out.println("Set học kỳ hiện tại thành học kỳ không hiện tại!");
                }
            }
        }
    }//GEN-LAST:event_btnTab4ActionPerformed

