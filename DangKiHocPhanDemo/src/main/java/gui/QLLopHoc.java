    private void btnThemTab2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemTab2ActionPerformed
        String maLop = tfTab2.getText();
        if (maLop.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Chưa nhập mã lớp!");
            return;
        }
        if (ClassDAO.layThongTinClass(maLop) != null) {
            JOptionPane.showMessageDialog(rootPane, "Mã lớp đã tồn tại!");
        } else {
            ClassPojo c = new ClassPojo();
            c.setMaLop(maLop);
            if (ClassDAO.addClass(c) == true) {
                JOptionPane.showMessageDialog(rootPane, "Thêm lớp học thành công!");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Thêm lớp học thất bại!");
            }
        }
    }//GEN-LAST:event_btnThemTab2ActionPerformed

    private void btnTab3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTab3ActionPerformed
        String maLop = cbTab3.getSelectedItem().toString();

        if (ClassDAO.xoaClass(maLop) == true) {
            JOptionPane.showMessageDialog(rootPane, "Xóa lớp học thành công!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Xóa lớp học thất bại!");
        }
    }//GEN-LAST:event_btnTab3ActionPerformed

