    private void btnTab1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTab1ActionPerformed
        List<course> listCourse = new ArrayList<>();
        for (int i = 0; i < modelTab1.getRowCount(); i++) {
            if ((boolean) modelTab1.getValueAt(i, 8) == true) {
                String maMonHoc = (String) modelTab1.getValueAt(i, 0);
                course c = CourseDAO.layThongTinCourse(maMonHoc);
                listCourse.add(c);
            }
        }

        if (listCourse.size() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Chưa đăng kí học phần nào!");
            return;
        } else if (listCourse.size() > 8) {
            JOptionPane.showMessageDialog(rootPane, "Chỉ được đăng kí tối đa 8 môn!");
            return;
        }

        for (int i = 0; i < listCourse.size() - 1; i++) {
            for (int j = i + 1; j < listCourse.size(); j++) {
                course x = listCourse.get(i);
                course y = listCourse.get(j);
                if (x.getCaHoc() == y.getCaHoc() && x.getNgayHoc().equals(y.getNgayHoc())) {
                    JOptionPane.showMessageDialog(rootPane, "Lịch học đã bị trùng, xem lại!");
                    return;
                }
            }
        }

        student st = StudentDAO.layThongTinSinhVien(login.staticUsername);
        if (st == null) {
            JOptionPane.showMessageDialog(rootPane, "Đăng kí học phần thất bại!");
        }

        for (course c : listCourse) {
            st.getCourse_student().add(c);
        }
        if (StudentDAO.capNhatThongTinStudent(st) == true) {
            JOptionPane.showMessageDialog(rootPane, "Đăng kí học phần thành công!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Đăng kí học phần thất bại!");
        }
    }//GEN-LAST:event_btnTab1ActionPerformed

    private void btnTab3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTab3ActionPerformed
        List<course> listCourse = new ArrayList<>();
        for (int i = 0; i < modelTab3.getRowCount(); i++) {
            if ((boolean) modelTab3.getValueAt(i, 7) == true) {
                String maMonHoc = (String) modelTab3.getValueAt(i, 0);
                course c = CourseDAO.layThongTinCourse(maMonHoc);
                listCourse.add(c);
            }
        }

        if (listCourse.size() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Chưa xóa học phần nào!");
            return;
        }

        student st = StudentDAO.layThongTinSinhVien(login.staticUsername);
        if (st == null) {
            JOptionPane.showMessageDialog(rootPane, "Xóa học phần thất bại!");
        }
        boolean flag = false;
        List<course> list = new ArrayList<>(st.getCourse_student());
        for (int i = 0; i < listCourse.size(); i++) {
            for (int j = 0; j < list.size(); j++) {
                if (listCourse.get(i).getMaMonHoc().equals(list.get(j).getMaMonHoc())) {
                    list.remove(j);
                    j--;
                    flag = true;
                }
            }
        }
        st.setCourse_student(new HashSet<>(list));
        if (StudentDAO.capNhatThongTinStudent(st) == true) {
            JOptionPane.showMessageDialog(rootPane, "Xóa học phần thành công!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Xóa học phần thất bại!");
        }
    }//GEN-LAST:event_btnTab3ActionPerformed

