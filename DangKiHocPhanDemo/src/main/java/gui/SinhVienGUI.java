    private void btnUpdateTab0ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateTab0ActionPerformed
        String name = tfNameTab0.getText();
        String address = tfAddressTab0.getText();
        int gioiTinh = cbTab0.getSelectedIndex();

        if (name.equals("") || address.equals("") || (gioiTinh != 0 && gioiTinh != 1)) {
            JOptionPane.showMessageDialog(rootPane, "Thông tin không được để trống!");
            return;
        }

        student st = StudentDAO.layThongTinSinhVien(login.staticUsername);
        if (st == null) {
            return;
        }
        st.setName(name);
        st.setAddress(address);
        st.setGioiTinh(gioiTinh);
        if (StudentDAO.capNhatThongTinStudent(st) == true) {
            JOptionPane.showMessageDialog(rootPane, "Cập nhật thông tin thành công!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Cập nhật thông tin thất bại!");
        }
    }//GEN-LAST:event_btnUpdateTab0ActionPerformed

